"""
Классификатор позиций по группам ТНВЭД.

Применяется к каждой строке исходного инвойса. Возвращает (tnved10, group_name).
Правила вынесены наверх, чтобы оператор мог их править под конкретный заказ.

Логика: правила применяются СТРОГО В ПОРЯДКЕ — первое сработавшее побеждает.
Это критично: например, "Bolt, metal" с большой массой в категории кузовных деталей
может быть отнесён к ЧАСТИ, а не к БОЛТАМ — добавьте такие исключения в начало.

Доказано на заказе SI0263:
  - ФИЛЬТРЫ сходится точно (1282 шт / 176.56 кг / 22 712.87 AED).
  - Остальные группы требуют ручной донастройки правил или ручной коррекции
    нескольких граничных позиций.
"""
import re

# (regex по объединённому описанию (eng+ru, lowercase), tnved10, group_ru)
RULES = [
    (r'\bsensor\b|датчик',                                              '9032900000', 'ДАТЧИКИ'),
    (r'filter|фильтр',                                                  '8421230000', 'ФИЛЬТРЫ'),
    (r'bearing|подшипник|\bhub unit\b|ступичный узел',                  '8482200009', 'ПОДШИПНИКИ'),
    (r'\bbelt\b|ремень|ремн|\btiming belt\b|\bv[- ]?belt\b|поликлин|клиновой|приводной ремень|serpentine|fan belt|drive belt', '4010310000', 'РЕМНИ'),
    (r'\bbolt\b|болт|\bnut\b|гайк|\bscrew\b|винт|\bwasher\b|шайб',     '7318159008', 'БОЛТЫ'),
    (r'bush|втулк|сайлент|grommet',                                     '7326909807', 'ВТУЛКИ'),
    (r'gasket|прокладк',                                                '4016995709', 'ПРОКЛАДКИ'),
    (r'\bseal\b|сальник|weatherstrip|уплотнит|o[- ]?ring|кольц',       '4016995709', 'УПЛОТНИТЕЛИ'),
]

DEFAULT_GROUP = ('8708999709', 'ЧАСТИ МОТОРНЫХ ТРАНС. СРЕДСТВ')

# Соответствие 8-значного HS-кода → (ТНВЭД 10 знаков, группа).
# Используется как fallback, когда описание неинформативно (например, в WENS-файлах
# описание общее "AUTO SPARE PARTS"). Для autoseller-файлов классификация всё равно
# идёт сначала по описанию (RULES), поэтому этот маппинг их не ломает.
HS_GROUP_MAP = {
    '87089900': ('8708999709', 'ЧАСТИ МОТОРНЫХ ТРАНС. СРЕДСТВ'),
    '84212300': ('8421230000', 'ФИЛЬТРЫ'),
    '40103100': ('4010310000', 'РЕМНИ'),
    '40103500': ('4010310000', 'РЕМНИ'),
    '40103900': ('4010310000', 'РЕМНИ'),
    '84822000': ('8482200009', 'ПОДШИПНИКИ'),
    '84821000': ('8482200009', 'ПОДШИПНИКИ'),
    '84825000': ('8482200009', 'ПОДШИПНИКИ'),
    '40169957': ('4016995709', 'УПЛОТНИТЕЛИ'),
    '40169310': ('4016995709', 'УПЛОТНИТЕЛИ'),
    '73269098': ('7326909807', 'ВТУЛКИ'),
    '73181590': ('7318159008', 'БОЛТЫ'),
    '90329000': ('9032900000', 'ДАТЧИКИ'),
    '27101982': ('2710198200', 'МАСЛО'),
}

# Список групп в порядке, в котором они должны появляться в INVOICE_TRANSIT.
# (tnved10, group_ru). Дубли по tnved разрешены (как УПЛОТНИТЕЛИ/ПРОКЛАДКИ оба = 4016995709).
GROUP_ORDER = [
    ('8708999709', 'ЧАСТИ МОТОРНЫХ ТРАНС. СРЕДСТВ'),
    ('8421230000', 'ФИЛЬТРЫ'),
    ('4010310000', 'РЕМНИ'),
    ('8482200009', 'ПОДШИПНИКИ'),
    ('4016995709', 'УПЛОТНИТЕЛИ'),
    ('4016995709', 'ПРОКЛАДКИ'),
    ('7326909807', 'ВТУЛКИ'),
    ('7318159008', 'БОЛТЫ'),
    ('9032900000', 'ДАТЧИКИ'),
    ('2710198200', 'МАСЛО'),
]


def classify_row(description_en: str, description_ru: str, hs_code: str):
    """Возвращает (tnved10, group_ru) для одной строки."""
    s_en = '' if description_en is None or (isinstance(description_en, float) and description_en != description_en) else str(description_en)
    s_ru = '' if description_ru is None or (isinstance(description_ru, float) and description_ru != description_ru) else str(description_ru)
    d = (s_en + ' | ' + s_ru).lower()
    hs = str(hs_code or '').strip()

    # МАСЛО — по HS-коду 2710 (смазочные/моторные масла). Надёжнее слова,
    # чтобы не путать с "oil filter" / "oil seal".
    if hs.startswith('2710'):
        return ('2710198200', 'МАСЛО')

    # МАСЛО — по описанию: "моторное масло" / "масло моторное" /
    # "трансмиссионное масло" / "трансмиссионная жидкость" (в любом порядке слов).
    # НЕ задевает "масляный фильтр", "масляный насос" и т.п. — там нет слова "масло"+"моторн/трансмиссион".
    if re.search(r'мотор\w*\s+масл|масл\w*\s+мотор|трансмиссион\w*\s+масл|'
                 r'масл\w*\s+трансмиссион|трансмиссион\w*\s+жидкост', d):
        return ('2710198200', 'МАСЛО')

    for pattern, tnved, group in RULES:
        if re.search(pattern, d):
            return (tnved, group)

    # Fallback по HS-коду (для файлов с неинформативным описанием)
    hs8 = hs[:8] if len(hs) >= 8 else hs
    if hs8 in HS_GROUP_MAP:
        return HS_GROUP_MAP[hs8]

    return DEFAULT_GROUP
